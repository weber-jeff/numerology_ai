# Define the data as you already did (soul_urge_meanings dictionary)

def get_soul_urge_analysis(number: int) -> dict:
    """Return soul urge number meaning including master numbers."""
    meaning = soul_urge_meanings.get(number)

    if not meaning:
        return {
            "summary": "Invalid Soul Urge number.",
            "advice": "Please enter a valid Soul Urge number (1-9, 11, 22, 33).",
            "master": False,
            "element": None,
            "traits": [],
            "strengths": [],
            "weaknesses": [],
            "business": None,
            "relationships": None,
            "purpose": None
        }

    return {
        "summary": f"Soul Urge Number {number} - {meaning['description']}",
        "advice": meaning['advice'],
        "master": meaning['master'],
        "element": meaning['element'],
        "traits": meaning['traits'],
        "strengths": meaning['strengths'],
        "weaknesses": meaning['weaknesses'],
        "business": meaning['business'],
        "relationships": meaning['relationships'],
        "purpose": meaning['purpose'],
    }

def get_soul_urge_report_string(number: int) -> str:
    """Generates and returns a formatted string report for a given Soul Urge number."""
    analysis = get_soul_urge_analysis(number)

    report_string = "=" * 60 + "\n"
    report_string += f"💖 Soul Urge Number {number} Report 💖\n"
    report_string += "=" * 60 + "\n"
    report_string += f"🧾 Summary: {analysis['summary']}\n"
    report_string += f"💡 Advice: {analysis['advice']}\n"
    report_string += f"🌟 Master Number: {'Yes' if analysis['master'] else 'No'}\n"
    report_string += f"🌿 Element: {analysis['element'] if analysis['element'] else 'Unknown'}\n"
    report_string += "\n🔑 Core Traits: " + ', '.join(analysis['traits']) + "\n"
    report_string += "✅ Strengths: " + ', '.join(analysis['strengths']) + "\n"
    report_string += "⚠️ Weaknesses: " + ', '.join(analysis['weaknesses']) + "\n"
    report_string += "\n💼 Business Outlook:\n"
    report_string += f" - {analysis['business']}\n"
    report_string += "\n❤️ Relationships:\n"
    report_string += f" - {analysis['relationships']}\n"
    report_string += "\n🎯 Life Purpose:\n"
    report_string += f" - {analysis['purpose']}\n"
    report_string += "=" * 60 + "\n"

    return report_string

